#multiple of 3, sum of all odd numbers from 1000 to 10000


#minimum,maximum = 1000,10000

total = 0

for n in range(1000,10001,3):
    
    if (n%2 != 0 ):
        
        print("{0}".format(n))
        
        total = total +n
        
print (" sum of odd numbers from {0} to {1} = {2}".format(1000,10001,total)) 
        
        
        
        


    
   
        
    

        


        
        
        
            
            
        
              
        
        
